def snake_to_pascal(name: str) -> str:
    return "".join(part[:1].upper() + part[1:] for part in name.split("_") if part)


def pascal_to_snake(name: str) -> str:
    return re.sub(r"(?<!^)(?=[A-Z])", "_", name).lower()


def fmt_number(value: float) -> str:
    """Deterministic float rendering for range doc comments."""
    if value == int(value):
        return f"{value:.1f}"
    return repr(value)


def range_comment_text(lo: float, hi: float) -> str:
    return f"range: {fmt_number(lo)}..={fmt_number(hi)}"


def coarse_type(t: IRType) -> str:
    """IR type -> coarse cross-language descriptor."""
    if t.kind == "primitive":
        if t.name == "string":
            return "string"
        if t.name == "bool":
            return "bool"
        return "number"
    if t.kind == "ref":
        return t.name
    if t.kind in ("list", "option"):
        return f"{t.kind}<{coarse_type(t.inner)}>"
    raise ValueError(f"un-coarsable type kind {t.kind!r}")


def expected_coarse_schema(ir: IR) -> dict:
    """The IR rendered into coarse comparison form — the gate's reference."""
    records = {
        rec.name: [
            {
                "name": f.name,
                "type": coarse_type(f.type),
                "range": [f.range.min, f.range.max] if f.range else None,
            }
            for f in rec.fields
        ]
        for rec in ir.records
    }
    variants = {var.name: list(var.cases) for var in ir.variants}
    return {"records": records, "variants": variants}


def parse_range_comment(text: str):
    """Extract ``[lo, hi]`` from a ``range: a..=b`` comment body, else None."""
    m = re.search(r"range:\s*([-\d.eE+]+)\.\.=([-\d.eE+]+)", text)
    if not m:
        return None
    return [float(m.group(1)), float(m.group(2))]
