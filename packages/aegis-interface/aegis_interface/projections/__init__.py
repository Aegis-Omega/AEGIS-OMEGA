"""Projection layer — RFC 0001 §6.

Each projection ``P_lang : IR -> source`` is a pure function and introduces no
semantic meaning the IR does not already carry. Alongside ``render`` every
projection exposes ``readback`` which re-extracts a *coarse comparison schema*
from its own generated source. The coarse schema captures only what all three
target type systems can express simultaneously — structural nesting
(``list`` / ``option``), reference targets, and a leaf category in
``{string, bool, number, <RefName>}``. TypeScript and Python collapse every
numeric width to a single type, so ``number`` is the strongest honest
cross-language leaf invariant. The equivalence gate (§8) asserts the three
readbacks and the IR's expected coarse schema are identical.
"""

